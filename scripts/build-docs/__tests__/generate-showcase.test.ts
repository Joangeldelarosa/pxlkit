// generateShowcase — discovery + write behaviour
// ---------------------------------------------------------------------------

describe("generateShowcase", () => {
  let tmp: string;

  beforeEach(async () => {
    tmp = await fs.mkdtemp(path.join(os.tmpdir(), "showcase-test-"));
  });

  afterEach(async () => {
    await fs.remove(tmp);
  });

  it("returns one output per manifest in dryRun mode without writing", async () => {
    const manifests = [
      makeManifestRecord({ name: "PixelButton" }),
      makeManifestRecord({
        name: "FancyCard",
        sourceFile: "/virt/packages/ui-kit/src/FancyCard.tsx",
      }),
    ];
    const res = await generateShowcase({
      repoRoot: tmp,
      manifests,
      dryRun: true,
    });
    expect(res.outputs).toHaveLength(2);
    expect(res.outputs.map((o) => o.componentName).sort()).toEqual([
      "FancyCard",
      "PixelButton",
    ]);
    // Nothing must have been written.
    const sectionsDir = path.join(
      tmp,
      "apps/web/src/app/ui-kit/sections",
    );
    expect(await fs.pathExists(sectionsDir)).toBe(false);
  });

  it("writes <Name>.section.tsx files when dryRun is false", async () => {
    const manifests = [makeManifestRecord({ name: "PixelButton" })];
    const res = await generateShowcase({
      repoRoot: tmp,
      manifests,
      dryRun: false,
    });
    expect(res.outputs).toHaveLength(1);
    const wrote = res.outputs[0]!.path;
    expect(wrote.endsWith("/PixelButton.section.tsx")).toBe(true);
    expect(await fs.pathExists(wrote)).toBe(true);
    const content = await fs.readFile(wrote, "utf8");
    expect(content).toContain("export default function PixelButtonSection");
  });

  it("falls back to .generated.section.tsx when a hand-authored file exists", async () => {
    const manifests = [makeManifestRecord({ name: "PixelButton" })];
    const sectionsDir = path.join(
      tmp,
      "apps/web/src/app/ui-kit/sections",
    );
    await fs.ensureDir(sectionsDir);
    const handAuthored = path.join(sectionsDir, "PixelButton.section.tsx");
    await fs.writeFile(
      handAuthored,
      "// hand-authored, do not touch\nexport default function X() { return null; }\n",
    );

    const res = await generateShowcase({
      repoRoot: tmp,
      manifests,
      dryRun: false,
    });
    expect(res.outputs).toHaveLength(1);
    expect(res.outputs[0]!.fellBackToGenerated).toBe(true);
    expect(res.outputs[0]!.path.endsWith("/PixelButton.generated.section.tsx"))
      .toBe(true);

    // Hand-authored file must be untouched.
    const after = await fs.readFile(handAuthored, "utf8");
    expect(after).toContain("hand-authored, do not touch");
  });

  it("overwrites a previously generated file (banner present) without falling back", async () => {
    const manifests = [makeManifestRecord({ name: "PixelButton" })];
    const sectionsDir = path.join(
      tmp,
      "apps/web/src/app/ui-kit/sections",
    );
    await fs.ensureDir(sectionsDir);
    const sectionPath = path.join(sectionsDir, "PixelButton.section.tsx");
    await fs.writeFile(
      sectionPath,
      "// AUTO-GENERATED by scripts/build-docs/generate-showcase.ts — DO NOT EDIT.\n// stale\n",
    );

    const res = await generateShowcase({
      repoRoot: tmp,
      manifests,
      dryRun: false,
    });
    expect(res.outputs).toHaveLength(1);
    expect(res.outputs[0]!.fellBackToGenerated).toBe(false);
    expect(res.outputs[0]!.path.endsWith("/PixelButton.section.tsx")).toBe(
      true,
    );
    const after = await fs.readFile(sectionPath, "utf8");
    expect(after).not.toContain("// stale");
    expect(after).toContain("export default function PixelButtonSection");
  });

  it("respects a custom outDir", async () => {
    const manifests = [makeManifestRecord({ name: "PixelButton" })];
    const res = await generateShowcase({
      repoRoot: tmp,
      manifests,
      outDir: "custom/sections",
      dryRun: false,
    });
    expect(res.outputs[0]!.path).toContain("/custom/sections/");
    expect(
      await fs.pathExists(
        path.join(tmp, "custom/sections/PixelButton.section.tsx"),
      ),
    ).toBe(true);
  });

  it("skips manifests whose name is not PascalCase", async () => {
    const bad = makeManifestRecord({ name: "pixelButton" });
    const res = await generateShowcase({
      repoRoot: tmp,
      manifests: [bad],
      dryRun: true,
    });
    expect(res.outputs).toHaveLength(0);
    expect(res.skipped).toHaveLength(1);
    expect(res.skipped[0]).toContain("invalid component name");
  });
});

// ---------------------------------------------------------------------------
// GenerateShowcaseGenerator — orchestrator wrapper
// ---------------------------------------------------------------------------

describe("GenerateShowcaseGenerator", () => {
  it("returns writes via the Generator.run contract without touching disk", async () => {
    const tmp = await fs.mkdtemp(
      path.join(os.tmpdir(), "showcase-gen-test-"),
    );
    try {
      const gen = new GenerateShowcaseGenerator();
      expect(gen.name).toBe("generate-showcase");
      const manifests = [makeManifestRecord({ name: "PixelButton" })];
      const result = await gen.run({
        repoRoot: tmp,
        manifests,
        outputs: new Map(),
        logger: {
          info: () => {},
          warn: () => {},
          error: () => {},
          success: () => {},
          table: () => {},
        },
      });
      expect(result.writes).toHaveLength(1);
      expect(result.writes[0]!.path).toMatch(/PixelButton\.section\.tsx$/);
      expect(result.writes[0]!.content).toContain(
        "export default function PixelButtonSection",
      );
      // No file was actually written (Generator.run runs in dryRun).
      const sectionsDir = path.join(tmp, "apps/web/src/app/ui-kit/sections");
      expect(await fs.pathExists(sectionsDir)).toBe(false);
    } finally {
      await fs.remove(tmp);
    }
  });
});
