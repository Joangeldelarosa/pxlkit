/**
 * Tests for generate-showcase.ts — emits one section file per manifest into
 * apps/web/src/app/ui-kit/sections/.
 *
 * These tests exercise:
 *   - renderShowcaseSection: pure template, no FS
 *   - generateShowcase: discovery + dryRun + write + safety fallback
 *   - GenerateShowcaseGenerator: Generator-base wrapper
 */

import fs from "fs-extra";
import os from "node:os";
import path from "node:path";
import { afterEach, beforeEach, describe, expect, it } from "vitest";
import {
  generateShowcase,
  GenerateShowcaseGenerator,
  renderShowcaseSection,
} from "../generate-showcase";
import type { ManifestRecord } from "../_lib/generator-base";

const StubComponent = () => null;

function makeManifestRecord(overrides: {
  name?: string;
  category?: string;
  status?: string;
  since?: string;
  description?: string;
  sourceFile?: string;
  examplesFile?: string;
  exampleIds?: string[];
}): ManifestRecord {
  const name = overrides.name ?? "PixelButton";
  const sourceFile =
    overrides.sourceFile ?? "/repo/packages/ui-kit/src/PixelButton.tsx";
  const examples = (overrides.exampleIds ?? ["default", "with-icon"]).map(
    (id) => ({
      id,
      label: id.replace(/-/g, " "),
      Component: StubComponent,
    }),
  );
  // The schema-typed manifest is wider than the loose generator-base shape;
  // cast through `unknown` so tests can author rich manifests while remaining
  // compatible with the ManifestRecord contract used by the generator.
  const manifest = {
    name,
    category: overrides.category ?? "actions",
    since: overrides.since ?? "1.0.0",
    status: overrides.status ?? "stable",
    description:
      overrides.description ??
      "Primary action button with retro pixel surface.",
    highlights: ["a", "b"],
    examples,
    props: "auto",
    a11y: {
      wcag: "2.1 AA",
      patterns: ["button"],
      keyboard: [{ key: "Enter", does: "activates" }],
    },
    related: [],
    apiStability: "stable",
    ssrSafe: true,
    treeShakable: true,
  } as unknown as ManifestRecord["manifest"];
  return {
    sourceFile,
    manifestFile: sourceFile.replace(/\.tsx$/, ".manifest.ts"),
    examplesFile: overrides.examplesFile,
    package: "@pxlkit/ui-kit",
    manifest,
  };
}

// ---------------------------------------------------------------------------
// renderShowcaseSection — pure template
// ---------------------------------------------------------------------------

describe("renderShowcaseSection", () => {
  it("emits the auto-generated banner with the source path", () => {
    const rec = makeManifestRecord({});
    const out = renderShowcaseSection({
      manifest: rec.manifest,
      sourceFile: rec.sourceFile,
      examplesFile: rec.examplesFile,
      sectionFile:
        "/repo/apps/web/src/app/ui-kit/sections/PixelButton.section.tsx",
    });
    expect(out).toContain(
      "AUTO-GENERATED by scripts/build-docs/generate-showcase.ts",
    );
    expect(out).toContain("/repo/packages/ui-kit/src/PixelButton.tsx");
  });

  it("uses the kebab-case anchor id and component name constants", () => {
    const rec = makeManifestRecord({ name: "PixelButton" });
    const out = renderShowcaseSection({
      manifest: rec.manifest,
      sourceFile: rec.sourceFile,
      examplesFile: rec.examplesFile,
      sectionFile:
        "/repo/apps/web/src/app/ui-kit/sections/PixelButton.section.tsx",
    });
    expect(out).toContain(`const COMPONENT_NAME = "PixelButton"`);
    expect(out).toContain(`const ANCHOR_ID = "pixel-button"`);
    expect(out).toContain(`const CATEGORY = "actions"`);
    expect(out).toContain(`const SINCE = "1.0.0"`);
    expect(out).toContain(`const STATUS = "stable"`);
  });

  it("exports a PascalCase default function suffixed with Section", () => {
    const rec = makeManifestRecord({ name: "FancyCard" });
    const out = renderShowcaseSection({
      manifest: rec.manifest,
      sourceFile: rec.sourceFile,
      examplesFile: rec.examplesFile,
      sectionFile:
        "/repo/apps/web/src/app/ui-kit/sections/FancyCard.section.tsx",
    });
    expect(out).toContain("export default function FancyCardSection");
  });

  it("renders all four tabs (preview / code / props / a11y)", () => {
    const rec = makeManifestRecord({});
    const out = renderShowcaseSection({
      manifest: rec.manifest,
      sourceFile: rec.sourceFile,
      examplesFile: rec.examplesFile,
      sectionFile:
        "/repo/apps/web/src/app/ui-kit/sections/PixelButton.section.tsx",
    });
    expect(out).toContain('data-tab="preview"');
    expect(out).toContain('data-tab="code"');
    expect(out).toContain('data-tab="props"');
    expect(out).toContain('data-tab="a11y"');
    expect(out).toMatch(/"preview", "code", "props", "a11y"/);
  });

  it("embeds the example id list in the EXAMPLE_LIST constant", () => {
    const rec = makeManifestRecord({
      exampleIds: ["default", "with-icon", "loading"],
    });
    const out = renderShowcaseSection({
      manifest: rec.manifest,
      sourceFile: rec.sourceFile,
      examplesFile: rec.examplesFile,
      sectionFile:
        "/repo/apps/web/src/app/ui-kit/sections/PixelButton.section.tsx",
    });
    expect(out).toContain('"id": "default"');
    expect(out).toContain('"id": "with-icon"');
    expect(out).toContain('"id": "loading"');
  });

  it("computes a relative import specifier from section to source", () => {
    const rec = makeManifestRecord({
      sourceFile: "/repo/packages/ui-kit/src/PixelButton.tsx",
    });
    const out = renderShowcaseSection({
      manifest: rec.manifest,
      sourceFile: rec.sourceFile,
      examplesFile: rec.examplesFile,
      sectionFile:
        "/repo/apps/web/src/app/ui-kit/sections/PixelButton.section.tsx",
    });
    // Should be a relative ../../ path with no .tsx extension.
    expect(out).toMatch(
      /import \* as Source from "\.\.\/(\.\.\/)+packages\/ui-kit\/src\/PixelButton";/,
    );
  });

  it("imports an Examples module when an examples file is provided", () => {
    const rec = makeManifestRecord({
      examplesFile: "/repo/packages/ui-kit/src/PixelButton.examples.tsx",
    });
    const out = renderShowcaseSection({
      manifest: rec.manifest,
      sourceFile: rec.sourceFile,
      examplesFile: rec.examplesFile,
      sectionFile:
        "/repo/apps/web/src/app/ui-kit/sections/PixelButton.section.tsx",
    });
    expect(out).toContain("import * as Examples from");
  });

  it("omits the Examples import when no examples file is present", () => {
    const rec = makeManifestRecord({ examplesFile: undefined });
    const out = renderShowcaseSection({
      manifest: rec.manifest,
      sourceFile: rec.sourceFile,
      examplesFile: rec.examplesFile,
      sectionFile:
        "/repo/apps/web/src/app/ui-kit/sections/PixelButton.section.tsx",
    });
    expect(out).not.toContain("import * as Examples from");
  });

  it("escapes embedded double quotes inside the description literal", () => {
    const rec = makeManifestRecord({
      description: 'Has "quotes" inside.',
    });
    const out = renderShowcaseSection({
      manifest: rec.manifest,
      sourceFile: rec.sourceFile,
      examplesFile: rec.examplesFile,
      sectionFile:
        "/repo/apps/web/src/app/ui-kit/sections/PixelButton.section.tsx",
    });
    expect(out).toContain(`const DESCRIPTION = "Has \\"quotes\\" inside."`);
  });
});

