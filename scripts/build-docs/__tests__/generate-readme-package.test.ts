/**
 * Unit tests for generate-readme-package.
 *
 * Strategy: use a temp repo on disk so we exercise filesystem discovery
 * (packages/* dirs, package.json parsing, README.generated.md writes) without
 * touching the real monorepo.
 */

import fs from "fs-extra";
import os from "node:os";
import path from "node:path";
import { afterEach, beforeEach, describe, expect, it } from "vitest";
import {
  generateReadmePackage,
  renderPackageReadme,
  renderComponentsBlock,
  fillComponentsBlock,
  COMPONENTS_START_MARKER,
  COMPONENTS_END_MARKER,
  GenerateReadmePackageGenerator,
  type PackageReadmeInput,
} from "../generate-readme-package";
import type { ManifestRecord } from "../_lib/generator-base";

