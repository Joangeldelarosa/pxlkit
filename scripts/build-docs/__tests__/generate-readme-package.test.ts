// generateReadmePackage (integration with temp fs)
// ---------------------------------------------------------------------------

describe("generateReadmePackage", () => {
  let tmp: string;

  beforeEach(async () => {
    tmp = await makeTempRepo();
  });

  afterEach(async () => {
    await fs.remove(tmp);
  });

  it("writes one README.generated.md per package with manifests", async () => {
    await makePkg(
      tmp,
      "ui-kit",
      { name: "@pxlkit/ui-kit", version: "1.0.0", license: "MIT" },
      { license: true, changelog: true },
    );
    await makePkg(tmp, "core", {
      name: "@pxlkit/core",
      version: "1.0.0",
      license: "MIT",
    });

    const result = await generateReadmePackage({
      repoRoot: tmp,
      manifests: [
        makeManifest("@pxlkit/ui-kit", "PixelButton"),
        makeManifest("@pxlkit/core", "PixelIcon"),
      ],
    });

    expect(result.outputs).toHaveLength(2);
    const paths = result.outputs.map((o) => o.path);
    expect(paths.some((p) => p.endsWith("packages/ui-kit/README.generated.md"))).toBe(
      true,
    );
    expect(paths.some((p) => p.endsWith("packages/core/README.generated.md"))).toBe(
      true,
    );

    const written = await fs.readFile(
      path.join(tmp, "packages", "ui-kit", "README.generated.md"),
      "utf8",
    );
    expect(written).toContain("# @pxlkit/ui-kit");
  });

  it("never overwrites a hand-authored README.md", async () => {
    const pkgDir = await makePkg(tmp, "ui-kit", {
      name: "@pxlkit/ui-kit",
      version: "1.0.0",
      license: "MIT",
    });
    const handAuthored = "# hand-authored — do not touch\n";
    await fs.writeFile(path.join(pkgDir, "README.md"), handAuthored, "utf8");

    await generateReadmePackage({
      repoRoot: tmp,
      manifests: [makeManifest("@pxlkit/ui-kit", "PixelButton")],
    });

    const after = await fs.readFile(path.join(pkgDir, "README.md"), "utf8");
    expect(after).toBe(handAuthored);
    expect(
      await fs.pathExists(path.join(pkgDir, "README.generated.md")),
    ).toBe(true);
  });

  it("fills the marker block in place when README.md declares it", async () => {
    const pkgDir = await makePkg(tmp, "ui-kit", {
      name: "@pxlkit/ui-kit",
      version: "1.0.0",
      license: "MIT",
    });
    const readme = [
      "# hand-authored title",
      "",
      "## Components",
      "",
      COMPONENTS_START_MARKER,
      "stale hand list",
      COMPONENTS_END_MARKER,
      "",
      "## License",
      "MIT",
      "",
    ].join("\n");
    await fs.writeFile(path.join(pkgDir, "README.md"), readme, "utf8");

    const result = await generateReadmePackage({
      repoRoot: tmp,
      manifests: [makeManifest("@pxlkit/ui-kit", "PixelButton")],
    });

    expect(result.outputs).toHaveLength(1);
    expect(result.outputs[0]!.path.endsWith("packages/ui-kit/README.md")).toBe(true);

    const after = await fs.readFile(path.join(pkgDir, "README.md"), "utf8");
    expect(after).toContain("# hand-authored title");
    expect(after).toContain("## License");
    expect(after).toContain(COMPONENTS_START_MARKER);
    expect(after).toContain(COMPONENTS_END_MARKER);
    expect(after).toContain("`PixelButton`");
    expect(after).not.toContain("stale hand list");
    // no sibling emitted in marker mode
    expect(await fs.pathExists(path.join(pkgDir, "README.generated.md"))).toBe(false);
  });

  it("falls back to the sibling when README.md has no markers", async () => {
    const pkgDir = await makePkg(tmp, "ui-kit", {
      name: "@pxlkit/ui-kit",
      version: "1.0.0",
      license: "MIT",
    });
    await fs.writeFile(path.join(pkgDir, "README.md"), "# no markers here\n", "utf8");

    const result = await generateReadmePackage({
      repoRoot: tmp,
      manifests: [makeManifest("@pxlkit/ui-kit", "PixelButton")],
    });

    expect(result.outputs).toHaveLength(1);
    expect(result.outputs[0]!.path.endsWith("README.generated.md")).toBe(true);
    const untouched = await fs.readFile(path.join(pkgDir, "README.md"), "utf8");
    expect(untouched).toBe("# no markers here\n");
  });

  it("skips packages that have no manifests", async () => {
    await makePkg(tmp, "ui-kit", {
      name: "@pxlkit/ui-kit",
      version: "1.0.0",
      license: "MIT",
    });
    await makePkg(tmp, "core", {
      name: "@pxlkit/core",
      version: "1.0.0",
      license: "MIT",
    });

    const result = await generateReadmePackage({
      repoRoot: tmp,
      manifests: [makeManifest("@pxlkit/ui-kit", "PixelButton")],
    });
    expect(result.outputs).toHaveLength(1);
    expect(result.skippedPackages).toContain("@pxlkit/core");
  });

  it("records packages with no/invalid package.json as failed", async () => {
    const broken = path.join(tmp, "packages", "broken");
    await fs.ensureDir(broken);
    // no package.json at all

    const result = await generateReadmePackage({
      repoRoot: tmp,
      manifests: [],
    });
    expect(result.failedPackages.some((p) => p.endsWith("packages/broken"))).toBe(
      true,
    );
  });

  it("dryRun=true returns outputs but writes nothing", async () => {
    const pkgDir = await makePkg(tmp, "ui-kit", {
      name: "@pxlkit/ui-kit",
      version: "1.0.0",
      license: "MIT",
    });
    const result = await generateReadmePackage({
      repoRoot: tmp,
      manifests: [makeManifest("@pxlkit/ui-kit", "PixelButton")],
      dryRun: true,
    });
    expect(result.outputs).toHaveLength(1);
    expect(
      await fs.pathExists(path.join(pkgDir, "README.generated.md")),
    ).toBe(false);
  });
});

