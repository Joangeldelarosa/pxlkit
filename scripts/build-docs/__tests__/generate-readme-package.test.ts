describe("GenerateReadmePackageGenerator", () => {
  it("is a Generator with the expected name", () => {
    const g = new GenerateReadmePackageGenerator();
    expect(g.name).toBe("generate-readme-package");
  });

  it("returns writes from the context manifests (dry-run)", async () => {
    const tmp = await makeTempRepo();
    try {
      await makePkg(tmp, "ui-kit", {
        name: "@pxlkit/ui-kit",
        version: "1.0.0",
        license: "MIT",
      });
      const g = new GenerateReadmePackageGenerator();
      const res = await g.run({
        repoRoot: tmp,
        manifests: [makeManifest("@pxlkit/ui-kit", "PixelButton")],
        outputs: new Map<string, string>(),
        logger: {
          info: () => {},
          warn: () => {},
          error: () => {},
          success: () => {},
          table: () => {},
        },
      });
      expect(res.writes).toHaveLength(1);
      expect(res.writes[0]!.path.endsWith("README.generated.md")).toBe(true);
      // dry-run: nothing written to disk
      expect(
        await fs.pathExists(
          path.join(tmp, "packages", "ui-kit", "README.generated.md"),
        ),
      ).toBe(false);
    } finally {
      await fs.remove(tmp);
    }
  });
});
