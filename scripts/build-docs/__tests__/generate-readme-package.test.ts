describe("renderPackageReadme", () => {
  const input: PackageReadmeInput = {
    packageName: "@pxlkit/ui-kit",
    packageDir: "/tmp/repo/packages/ui-kit",
    packageJson: {
      name: "@pxlkit/ui-kit",
      version: "1.9.0",
      description: "Retro pixel art UI kit",
      license: "MIT",
      homepage: "https://pxlkit.xyz",
      repository: "https://github.com/joangel/pxlkit",
    },
    manifests: [
      makeManifest("@pxlkit/ui-kit", "PixelButton"),
      makeManifest("@pxlkit/ui-kit", "PixelCard", {
        status: "beta",
        category: "cards",
      }),
    ],
  };

  it("renders a title with the package name", () => {
    const md = renderPackageReadme(input);
    expect(md.startsWith("# @pxlkit/ui-kit")).toBe(true);
  });

  it("includes a generator notice comment", () => {
    const md = renderPackageReadme(input);
    expect(md).toContain(
      "<!-- generated by scripts/build-docs/generate-readme-package.ts -->",
    );
  });

  it("emits npm + bundle + license badges", () => {
    const md = renderPackageReadme(input);
    expect(md).toContain("img.shields.io/npm/v/");
    expect(md).toContain("img.shields.io/bundlephobia/minzip/");
    expect(md).toContain("img.shields.io/badge/license-MIT-blue.svg");
  });

  it("renders the package description verbatim", () => {
    const md = renderPackageReadme(input);
    expect(md).toContain("Retro pixel art UI kit");
  });

  it("includes install snippets for npm/pnpm/yarn", () => {
    const md = renderPackageReadme(input);
    expect(md).toContain("npm install @pxlkit/ui-kit");
    expect(md).toContain("pnpm add @pxlkit/ui-kit");
    expect(md).toContain("yarn add @pxlkit/ui-kit");
  });

  it("synthesises a quick start from the first stable component", () => {
    const md = renderPackageReadme(input);
    expect(md).toContain('import { PixelButton } from "@pxlkit/ui-kit";');
    expect(md).toContain("<PixelButton />");
  });

  it("renders a components table sorted by name", () => {
    const md = renderPackageReadme(input);
    expect(md).toContain(
      "| Component | Status | Since | Category | Description |",
    );
    const buttonIdx = md.indexOf("PixelButton");
    const cardIdx = md.indexOf("PixelCard");
    expect(buttonIdx).toBeGreaterThan(0);
    expect(cardIdx).toBeGreaterThan(buttonIdx);
  });

  it("escapes pipe characters inside descriptions", () => {
    const md = renderPackageReadme({
      ...input,
      manifests: [
        makeManifest("@pxlkit/ui-kit", "Weird", {
          description: "has a | pipe in it",
        }),
      ],
    });
    expect(md).toContain("has a \\| pipe in it");
  });

  it("emits an api stability commitment section", () => {
    const md = renderPackageReadme(input);
    expect(md).toContain("## API stability");
  });

  it("emits a links section with homepage and repo", () => {
    const md = renderPackageReadme(input);
    expect(md).toContain("[Homepage](https://pxlkit.xyz)");
    expect(md).toContain("[Repository](https://github.com/joangel/pxlkit)");
  });

  it("handles repository as an object with url", () => {
    const md = renderPackageReadme({
      ...input,
      packageJson: {
        ...input.packageJson,
        repository: { type: "git", url: "https://example.com/repo.git" },
      },
    });
    expect(md).toContain("[Repository](https://example.com/repo.git)");
  });

  it("falls back to a generic quick start when there are no manifests", () => {
    const md = renderPackageReadme({ ...input, manifests: [] });
    expect(md).toContain("_No components registered yet._");
    expect(md).toContain('import * as pxlkitUiKit from "@pxlkit/ui-kit";');
  });
});

