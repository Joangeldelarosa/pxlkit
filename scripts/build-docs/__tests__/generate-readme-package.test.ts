function makeManifest(
  pkg: string,
  name: string,
  overrides: Partial<ManifestRecord["manifest"]> = {},
): ManifestRecord {
  const tsx = `/tmp/${pkg}/src/${name}.tsx`;
  return {
    package: pkg,
    sourceFile: tsx,
    manifestFile: tsx.replace(/\.tsx$/, ".manifest.ts"),
    manifest: {
      name,
      description: `description for ${name}`,
      category: "actions",
      status: "stable",
      since: "1.0.0",
      ...overrides,
    },
  };
}

async function makeTempRepo(): Promise<string> {
  const dir = await fs.mkdtemp(path.join(os.tmpdir(), "pxl-readme-test-"));
  await fs.ensureDir(path.join(dir, "packages"));
  return dir;
}

async function makePkg(
  repoRoot: string,
  dirName: string,
  pkgJson: Record<string, unknown>,
  extras: { license?: boolean; changelog?: boolean } = {},
): Promise<string> {
  const pkgDir = path.join(repoRoot, "packages", dirName);
  await fs.ensureDir(pkgDir);
  await fs.writeJson(path.join(pkgDir, "package.json"), pkgJson, { spaces: 2 });
  if (extras.license) {
    await fs.writeFile(path.join(pkgDir, "LICENSE"), "MIT", "utf8");
  }
  if (extras.changelog) {
    await fs.writeFile(
      path.join(pkgDir, "CHANGELOG.md"),
      "# changelog\n",
      "utf8",
    );
  }
  return pkgDir;
}

